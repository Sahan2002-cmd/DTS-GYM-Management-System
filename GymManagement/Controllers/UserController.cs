        // POST /User/Edit
        [HttpPost]
        public ActionResult Edit(UserRequestModel req)
        {
            if (req.p_admin_id == null)
                return Json(new { StatusCode = 400, Message = "p_admin_id is required." });

            int loggedIn = req.p_admin_id.Value;
            bool isAdmin = _user.IsAdmin(loggedIn);

            if (!isAdmin && loggedIn != req.p_user_id)
                return Json(new { StatusCode = 403, Message = "You can only edit your own profile." });

            return Json(_user.EditUser(req));
        }

        // POST /User/Delete?id=1&adminId=1
        [HttpPost]
        public ActionResult Delete(int id, int adminId)
        {
            if (!_user.IsAdmin(adminId))
                return Json(new { StatusCode = 403, Message = "Unauthorized. Admin access required." });
            return Json(_user.DeleteUser(id, adminId));
        }

        // ── Admin Status Management ─────────────────────────────────

        // POST /User/ApproveUser
        // newStatus: "active" (approve) | "rejected" | "inactive" | "suspended" | "pending"
        [HttpPost]
        public ActionResult ApproveUser(int userId, int adminId, string newStatus,
                                        int roleId, string firstName = "", string lastName = "")
        {
            if (!_user.IsAdmin(adminId))
                return Json(new { StatusCode = 403, Message = "Unauthorized. Admin access required." });

            var valid = new[] { "active", "rejected", "inactive", "suspended", "pending" };
            if (string.IsNullOrWhiteSpace(newStatus) || Array.IndexOf(valid, newStatus) < 0)
                return Json(new { StatusCode = 400, Message = "Invalid status." });

            return Json(_user.ApproveOrRejectUser(userId, adminId, newStatus,
                                                   roleId, firstName, lastName));
        }

        // POST /User/ChangeUserStatus?userId=5&adminId=1&newStatus=suspended
        [HttpPost]
        public ActionResult ChangeUserStatus(int userId, int adminId, string newStatus)
        {
            if (!_user.IsAdmin(adminId))
                return Json(new { StatusCode = 403, Message = "Unauthorized. Admin access required." });

            var valid = new[] { "active", "inactive", "suspended", "pending", "rejected" };
            if (string.IsNullOrWhiteSpace(newStatus) || Array.IndexOf(valid, newStatus) < 0)
                return Json(new { StatusCode = 400, Message = "Invalid status." });

            return Json(_user.ChangeUserStatus(userId, adminId, newStatus));
        }

        // POST /User/ChangeLinkedStatus?userId=5&adminId=1&tableName=Member&newStatus=inactive
        [HttpPost]
        public ActionResult ChangeLinkedStatus(int userId, int adminId,
                                               string tableName, string newStatus)
        {
            if (!_user.IsAdmin(adminId))
                return Json(new { StatusCode = 403, Message = "Unauthorized. Admin access required." });

            if (tableName != "Member" && tableName != "Trainer")
                return Json(new { StatusCode = 400, Message = "tableName must be Member or Trainer." });

            return Json(_user.ChangeLinkedTableStatus(userId, adminId, tableName, newStatus));
        }

