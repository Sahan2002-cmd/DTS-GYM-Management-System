        [HttpPost]
        public ActionResult Register(UserRequestModel req)
        {
            if (Request.Form["p_role_id"] != null && int.TryParse(Request.Form["p_role_id"], out int rid))
                req.p_role_id = rid;

            if (string.IsNullOrWhiteSpace(req.p_email) || string.IsNullOrWhiteSpace(req.p_password_hash))
                return Json(new { StatusCode = 400, Message = "Email and password are required." });

            var response = _user.AddUser(req);
            return Json(response, JsonRequestBehavior.AllowGet);
        }
