        // ── Image Upload ────────────────────────────────────────────
        // POST /User/UploadImage
        // Accepts: multipart form with field "file"
        // Saves to ~/Uploads/Users/<guid>.<ext>
        // Returns: { StatusCode, imagePath }
        // Limit: 5 MB enforced here and in Web.config maxRequestLength
        // POST /User/UploadImage
        // POST /User/UploadImage
        [HttpPost]
        [AllowAnonymous]
        [AllowCors] // ← CRITICAL for public registration
        public ActionResult UploadImage()
        {
            const long MaxBytes = 5 * 1024 * 1024;      // 5 MB

            try
            {
                // 1. Check if any file was sent
                if (Request.Files.Count == 0 || Request.Files[0].ContentLength == 0)
                    return Json(new { StatusCode = 400, Result = "No file received." });

                HttpPostedFileBase file = Request.Files[0];

                // 2. Size check
                if (file.ContentLength > MaxBytes)
                    return Json(new { StatusCode = 400, Result = $"File exceeds {MaxBytes / 1024 / 1024} MB limit." });

                // 3. Extension check
                string ext = Path.GetExtension(file.FileName).ToLower();
                string[] allowed = { ".jpg", ".jpeg", ".png", ".webp" };
                if (!allowed.Contains(ext))
                    return Json(new { StatusCode = 400, Result = "Only JPG, PNG, or WebP images are allowed." });

                // 4. Ensure directory exists
                string folder = Server.MapPath("~/Uploads/Users/");
                if (!Directory.Exists(folder))
                    Directory.CreateDirectory(folder);

                // 5. Save with unique name
                string fileName = Guid.NewGuid().ToString("N") + ext;
                string fullPath = Path.Combine(folder, fileName);
                file.SaveAs(fullPath);

                string relativePath = "/Uploads/Users/" + fileName;

                // 6. Return consistent JSON
                return Json(new
                {
                    StatusCode = 200,
                    Result = "Uploaded.",
                    imagePath = relativePath
                }, JsonRequestBehavior.AllowGet);
            }
            catch (Exception ex)
            {
                // Log the error (use your logging framework)
                // e.g., logger.Error(ex, "Image upload failed");
                return Json(new
                {
                    StatusCode = 500,
                    Result = "Upload failed: " + ex.Message
                }, JsonRequestBehavior.AllowGet);
            }
        }

        // ── Auth ────────────────────────────────────────────────────

        // POST /User/Login
        [HttpPost]
        public ActionResult Login(UserRequestModel req)
        {
            if (string.IsNullOrWhiteSpace(req.p_email) ||
                string.IsNullOrWhiteSpace(req.p_password_hash))
                return Json(new { StatusCode = 400, Message = "Email/Phone and password are required." });

            return Json(_user.Login(req.p_email, req.p_password_hash),
                        JsonRequestBehavior.AllowGet);
        }

        // POST /User/OAuthLogin
        [HttpPost]
        public ActionResult OAuthLogin(string providerName, string providerUserId,
                                       string email, string name)
        {
            if (string.IsNullOrWhiteSpace(email))
                return Json(new { StatusCode = 400, Message = "Email is required." });

            return Json(_user.OAuthLogin(providerName, providerUserId, email, name),
                        JsonRequestBehavior.AllowGet);
        }

